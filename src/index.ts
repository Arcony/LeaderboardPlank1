interface Notification {
    _id: string;
    category: string;
    actor?: string;
    receivers: string[];
    slug: string;
    text: string;
    image?: string;
    redirection?: string;
    datas: {
        category: string;
        slug: string;
        quantity: number;
    };
    createdAt: string;
    updatedAt: string;
    __v: number;
    read: boolean;
}

// Définir les timestamps fournis (en secondes) et les convertir en millisecondes
const startTimestamp = 1724171429 * 1000; // Début de la période
const endTimestamp = 1724272229 * 1000; // Fin de la période

interface AggregatedNotification {
    actor: string;
    totalQuantity: number;
}

// Fonction pour extraire le nom de l'acteur depuis le texte
function extractActor(text: string): string {
    const words = text.split(' ');
    return words[0];
}

// Fonction pour filtrer les notifications par date en utilisant les timestamps, et en éliminant les doublons
function filterNotificationsByDate(notifications: Notification[], startTimestamp: number, endTimestamp: number): Notification[] {
    const uniqueCreatedAt: Record<string, boolean> = {};

    return notifications.filter(notification => {
        const createdAtTimestamp = new Date(notification.createdAt).getTime();

        // Vérifier que la notification est dans la plage de temps et n'a pas déjà été traitée
        if (createdAtTimestamp >= startTimestamp && createdAtTimestamp < endTimestamp) {
            if (!uniqueCreatedAt[notification.createdAt]) {
                uniqueCreatedAt[notification.createdAt] = true;
                return true;
            }
        }
        return false;
    });
}

// Fonction pour agréger les notifications par 'wood_plank'
function aggregateWoodPlanks(notifications: Notification[]): AggregatedNotification[] {
    const grouped: Record<string, AggregatedNotification> = {};

    notifications.forEach(notification => {
        if (notification.datas.slug === 'wood_plank') {
            const actor = extractActor(notification.text);
            if (!grouped[actor]) {
                grouped[actor] = {
                    actor: actor,
                    totalQuantity: 0
                };
            }
            grouped[actor].totalQuantity += notification.datas.quantity;
        }
    });

    // Alternative to Object.values
    const resultArray = [];
    for (const key in grouped) {
        if (grouped.hasOwnProperty(key)) {
            resultArray.push(grouped[key]);
        }
    }

    return resultArray.sort((a, b) => b.totalQuantity - a.totalQuantity);
}

// Fonction pour déterminer la récompense potentielle basée sur le rang
function getRewardImage(rank: number): string {
    if (rank === 0) {
        return 'top1.png'; // Saphir pour les deux premiers
    } else if (rank === 1) {
        return 'saphir.png'; // Rubis pour le troisième
    } else if (rank === 2) {
        return 'ruby.png'; // Rubis pour le troisième
    }
    return ''; // Pas de récompense pour les autres
}

// Fonction pour rendre les données agrégées dans le tableau HTML avec une colonne de récompense
function renderAggregatedWoodPlanks(aggregated: AggregatedNotification[]) {
    const tbody = document.getElementById('inventory-body');
    if (!tbody) return;

    tbody.innerHTML = ''; // Effacer les lignes existantes

    aggregated.slice(0, 50).forEach((notification, index) => {
        const rankClass = index === 0 ? 'rank-top' :
                          index === 1 ? 'rank-second' :
                          index === 2 ? 'rank-third' : '';
        
        const rewardImage = getRewardImage(index);

        const row = document.createElement('tr');
        row.innerHTML = `
            <td class="${rankClass}">${index + 1}</td>
            <td class="${rankClass}">${notification.actor}</td>
            <td class="${rankClass}">${notification.totalQuantity}</td>
            <td class="${rankClass}">
                ${rewardImage ? `<img src="img/${rewardImage}" width="32">` : ''}
            </td>
        `;
        tbody.appendChild(row);
    });
}

