const startDate = new Date(); // Aujourd'hui
startDate.setHours(0, 0, 0, 0); // Début de la journée

const endDate = new Date(); // Demain
endDate.setDate(startDate.getDate() + 1);
endDate.setHours(0, 0, 0, 0); // Début de la journée de demain

interface AggregatedNotification {
    actor: string;
    totalQuantity: number;
}

function extractActor(text: string): string {
    const words = text.split(' ');
    return words[0];
}

function filterNotificationsByDate(notifications: Notification[], startDate: Date, endDate: Date): Notification[] {
    return notifications.filter(notification => {
        const createdAt = new Date(notification.createdAt);
        return createdAt >= startDate && createdAt < endDate;
    });
}

function aggregateWoodPlanks(notifications: Notification[]): AggregatedNotification[] {
    const grouped: Record<string, AggregatedNotification> = {};

    notifications.forEach(notification => {
        if (notification.data.slug === 'wood_plank') {
            const actor = extractActor(notification.text);
            if (!grouped[actor]) {
                grouped[actor] = {
                    actor: actor,
                    totalQuantity: 0
                };
            }
            grouped[actor].totalQuantity += notification.data.quantity;
        }
    });

    return Object.values(grouped).sort((a, b) => b.totalQuantity - a.totalQuantity);
}

function renderAggregatedWoodPlanks(aggregated: AggregatedNotification[]) {
    const tbody = document.getElementById('inventory-body');
    if (!tbody) return;

    tbody.innerHTML = ''; // Clear existing rows

    aggregated.slice(0, 50).forEach((notification, index) => {
        const rankClass = index === 0 ? 'rank-top' :
                          index === 1 ? 'rank-second' :
                          index === 2 ? 'rank-third' : '';

        const row = document.createElement('tr');
        row.innerHTML = `
            <td class="${rankClass}">${index + 1}</td>
            <td class="${rankClass}">${notification.actor}</td>
            <td class="${rankClass}">${notification.totalQuantity}</td>
        `;
        tbody.appendChild(row);
    });
}

// Filtrer les notifications par date et les afficher
const filteredNotifications = filterNotificationsByDate(notifications, startDate, endDate);
const aggregatedWoodPlanks = aggregateWoodPlanks(filteredNotifications);
renderAggregatedWoodPlanks(aggregatedWoodPlanks);