// Définir les timestamps fournis (en secondes) et les convertir en millisecondes
var startTimestamp = 1724171429 * 1000; // Début de la période
var endTimestamp = 1724272229 * 1000; // Fin de la période
// Fonction pour extraire le nom de l'acteur depuis le texte
function extractActor(text) {
    var words = text.split(' ');
    return words[0];
}
// Fonction pour filtrer les notifications par date en utilisant les timestamps
function filterNotificationsByDate(notifications, startTimestamp, endTimestamp) {
    return notifications.filter(function (notification) {
        var createdAtTimestamp = new Date(notification.createdAt).getTime();
        return createdAtTimestamp >= startTimestamp && createdAtTimestamp < endTimestamp;
    });
}
// Fonction pour agréger les notifications par 'wood_plank'
function aggregateWoodPlanks(notifications) {
    var grouped = {};
    notifications.forEach(function (notification) {
        if (notification.datas.slug === 'wood_plank') {
            var actor = extractActor(notification.text);
            if (!grouped[actor]) {
                grouped[actor] = {
                    actor: actor,
                    totalQuantity: 0
                };
            }
            grouped[actor].totalQuantity += notification.datas.quantity;
        }
    });
    // Alternative to Object.values
    var resultArray = [];
    for (var key in grouped) {
        if (grouped.hasOwnProperty(key)) {
            resultArray.push(grouped[key]);
        }
    }
    return resultArray.sort(function (a, b) { return b.totalQuantity - a.totalQuantity; });
}
// Fonction pour rendre les données agrégées dans le tableau HTML
function renderAggregatedWoodPlanks(aggregated) {
    var tbody = document.getElementById('inventory-body');
    if (!tbody)
        return;
    tbody.innerHTML = ''; // Effacer les lignes existantes
    aggregated.slice(0, 50).forEach(function (notification, index) {
        var rankClass = index === 0 ? 'rank-top' :
            index === 1 ? 'rank-second' :
                index === 2 ? 'rank-third' : '';
        var row = document.createElement('tr');
        row.innerHTML = "\n            <td class=\"".concat(rankClass, "\">").concat(index + 1, "</td>\n            <td class=\"").concat(rankClass, "\">").concat(notification.actor, "</td>\n            <td class=\"").concat(rankClass, "\">").concat(notification.totalQuantity, "</td>\n        ");
        tbody.appendChild(row);
    });
}
// Filtrer les notifications par date et les afficher
var filteredNotifications = filterNotificationsByDate(notifications, startTimestamp, endTimestamp);
var aggregatedWoodPlanks = aggregateWoodPlanks(filteredNotifications);
renderAggregatedWoodPlanks(aggregatedWoodPlanks);
